<?php

//Aritmeticos
$salario = 2000;

//incrementando um bonus
$salario = $salario + 200; //2200 

//outra forma mais prática de incrementar
$salario += 200;

//forma ainda mais prática de incrementar de 1 em 1
$salario ++;

//decremento
//pode fazer o mesmo com multiplicação e divisão
$salario -= 500;
//forma ainda mais prática de decrementar de 1 em 1
$salario --;
